package model;

public interface IDimension {
	/**
	 * Get the width
	 * 
	 * @return width
	 */
	public int getwidth();

	/**
	 * Get he height
	 * 
	 * @return height
	 */

	public int getheight();

	/**
	 * Set the width
	 * 
	 * @param width
	 */

	public void setwidth(int width);

	/**
	 * Set the height
	 * 
	 * @param height
	 */

	public void setheight(int height);
}
