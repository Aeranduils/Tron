package model;

/**
 * 
 * @author flori
 *
 */
public interface IPosition {
	/**
	 * Get X position
	 * 
	 * @return X
	 */
	int getX();

	/**
	 * Get Y position
	 * 
	 * @return Y
	 */

	int getY();

	/**
	 * Set the X position
	 * 
	 * @param x
	 */

	void setX(int x);

	/**
	 * Set the Y position
	 * 
	 * @param y
	 */
	void setY(int y);

}
